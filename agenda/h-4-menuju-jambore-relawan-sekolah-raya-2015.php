        <p class="indent">Respon dan atusiasme yang diberikan oleh sejumlah peserta undangan memang lah cukup membuat panitia tergagap dalam menyiapkan agenda Jambore Relawan Sekolah Raya tahun ini, yang mengusung tema "Merekam Jejak Relawan" .</p>

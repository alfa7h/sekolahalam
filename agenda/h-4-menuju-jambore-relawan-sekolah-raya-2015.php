        <p class="indent">DJika tahun lalu sebagai tahun pertama Sekolah Raya dalam menyelenggarakan Jambore Relawan dengan tema “Visioning Sekolah Raya” diikuti oleh 50 peserta yang baru sebatas berasal dari Jakarta, Bekasi, dan Malang saja.</p>
        <p class="indent">Di tahun ini lebih dari 70 peserta yang memberikan konfirmasi kehadiran tidak hanya datang dari kawan-kawan penggiat literasi dan pendidikan yang berdomisili di Jabodetabek. Tidak juga hanya dari kawan-kawan di Pulau Jawa seperti Banten, Bandung, Cirebon, Kediri, dan Malang.  Tetapi juga dari kawan-kawan perwakilan di Kalimantan Tengah, Sulawesi Tengah, Nusa Tenggara Timur,  hingga Papua.</p>
        <p class="indent">Tak ketinggalan, narasumber ternama dengan kompetensi yang sesuai dengan tema Jambore Relawan Sekolah Raya tahun ini pun berkenan berbagi bersama dalam kegiatan ini. Mereka diantaranya adalah Muhammad Farhan (presenter), Indah Julianti Sibarani (blogger), Ahmad Fuadi (penulis), Fauzan Mukrim (penulis), dan Stanley Meulen (penulis).</p>
        <p class="indent">Maka tidaklah berlebihan jika di H-4 menuju Jambore Relawan Sekolah Raya 2015, kami dengan segenap kerendahan hati tetap menjaga harapan dalam do’a, semoga alam semesta memberikan dukungan positif bagi pelaksanaan acara ini.</p>

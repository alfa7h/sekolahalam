        <h4>Hour of Code 2015 di Sekolah Raya</h4>
        <p ><b><i>Hour of Code 2015 </i></b>adalah gerakan global memberdayakan 200 juta anak di lebih dari 180 negara untuk belajar memprogram komputer pada bulan Desember. Di Indonesia <i>Hour of Code</i> 2015 diselenggarakan serentak di seluruh Indonesia antara tanggal 12-20 Desember 2015 secara gratis untuk setiap siswa dan menjadi gerakan nasional Hour of Code terbesar di dunia.</p>
        <p>selama pelaksanaan, anak-anak dan orang dewasa diarahkan untuk bermain <i>game coding</i> dalam kelompok belajar, secara <i>online / offline</i> , sehingga mereka dapat belajar prinsip dasar dari logika dan algoritma pemrograman secara menyenangkan. Kegiatan ini memungkinkan semua orang, dari siswa, guru dan orang tua untuk belajar <i>coding</i>.</p>
        <img src="images/agenda/hour-of-code-2015/img1.jpg" style="margin: auto; width: 100%; height:auto" class="img-artikel2" >
        <p>Prinsip di balik event <i>Hour of Code</i> adalah untuk membentuk kesadaran, membuat siswa, guru, orang tua dan komunitas berani dan percaya diri dalam kondisi mereka saat ini, di sekolah dan komunitas mereka saat ini untuk belajar <i>coding</i>. Kekuatan untuk menciptakan ini sangat kuat di setiap anak muda dan banyak sekali pihak yang suka dengan kesempatan untuk dapat "menciptakan" sesuatu melalui <i>coding</i>.</p> 
        <p>Dengan motivasi bahwa setiap anak berhak mendapat kesempatan pembelajaran yang sama, tidak terkecuali bagi anak-anak kaum marjinal dan dhuafa, Sekolah Raya pun ambil bagian dalam rangkaian <i>Hour of Code</i> Indonesia. Dipandu oleh relawan-relawan Sekolah Raya yang sebelumnya telah mengikuti <i>Training the Trainer (TTT)</i> pada Rabu, 9 Desember 2015 lalu di kantor redaksi salah satu harian nasional, Sekolah Raya menggelar acara <i>Hour of Code</i> bagi siswa-siswa dari jejaringnya seperti SMP Terbuka Tarumajaya, SMP Terbuka Ilalang, dan Sekolah Alam Anak Soleh pada Sabtu, 19 Desember 2015 di Rumah Baca HOS Tjokroaminoto, Villa Mutiara Gading I Blok H, Desa Setia Asih, Tarumajaya, Bekasi..</p>

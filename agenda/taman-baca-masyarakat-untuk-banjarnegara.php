        <h4>Taman Baca Masyarakat untuk Banjarnegara</h4>
        <img src="images/agenda/taman-baca-masyarakat-untuk-banjarnegara/img1.jpg" class="responsive-img img-artikel2" >
        <p class="indent">TBM (Taman Baca Masyarakat) merupakan sarana pembudayaan kegemaran membaca masyarakat yang menyediakan dan memberikan layanan di bidang bahan bacaan. Dilengkapi dengan ruangan untuk membaca, diskusi, bedah buku, menulis, dan kegiatan literasi lainnya, dan didukung oleh pengelola yang berperan sebagai motivator. Pembangunan TBM merupakan wujud dari upaya penguatan dan perluasan budaya membaca karena buku adalah salah satu cara untuk mendidik generasi berikutnya, menanamkan kepribadian, etos kerja, dan integritas diri. Bacaan yang baik akan membuka mata, dan menumbuhkan antusiasme anak-anak hingga orang dewasa. Oleh karena itu diharapkan masyarakat akan tumbuh dengan pemahaman yang baik melalui buku-buku baik tersebut. Selain itu TBM dapat pula dijadikan salah satu sarana edukasi informal diluar sekolah dan lembaga pendidikan lainnya, oleh sebab itu berdirinya TBM di suatu lingkungan bermasyarakat menjadi sebuah urgensi dalam upaya menumbuhkan budaya literasi, nilai-nilai sosial, dan edukasi.</p>
        <p class="indent">Sekolah Raya sebagai sebuah jejaring komunitas yang fokus terhadap kegiatan sosial berbasis pendidikan, menginisiasi pembangunan TBM di desa Jemblung, Banjarnegara.Bencana alam longsor di Banjarnegara pada tanggal 13 Desember lalu telah meluluhlantakkan desa Jemblung, ratusan penduduk kehilangan tempat tinggal dan mangalami trauma psikososial. Pembangunan TBM di desa Jemblung merupakan upaya program recovery paska bencana. Dalam jangka panjang, eksistensi TBM diharapkan mampu mengembalikan roda kehidupan sosial masyarakat dalam upaya pemulihan desa  serta dapat mengembalikan rasa optimisme dan percaya diri masyarakat melalui buku-buku inspiratif dan kegiatan edukatif diTBM.</p>
        <p class="indent"><b>TUJUAN</b> <br>1. Menumbuhkan budaya literasi;<br>2. Menciptakan ruang edukasi, meningkatkan kreatifitas, dan sosialisasi bagi masyarakat setempat;<br>3. Sebagai upaya recovery paska bencana.</p>
        <p class="indent"><b>KEGIATAN</b><br>Proses identifikasi, perencanaan, dan pengumpulan donasi dimulai pada bulan Desember 2014 dan penyerahan donasi pembangunan TBM secara simbolis akan dilaksanakan tanggal 29 Desember 2014 pada saat penutupan acara “Jambore Anak Tangguh” (merupakan serangkaian kegiatan Sekolah Raya dalam upaya pemulihan trauma psikososial bagi anak-anak korban bencana longsor di Banjarnegara).</p>
        <img src="images/agenda/taman-baca-masyarakat-untuk-banjarnegara/img2.jpg" class="responsive-img img-artikel2" >
        <p class="indent"><b>KONSEP TAMAN BACA MASYARAKAT</b><br>TBM terdiri dari 3 besaran ruang dengan taman bermain di depan bangunannya. Adapun ruangan  tersebut adalah:</p> 
        <p class="indent"><B>Ruang Baca</B>
        <br>Berisi koleksi buku-buku TBM dan sebagai ruang membaca,terdapat sofa ataupun bisa membaca secara lesehan
        <BR><b>Ruang Kreasi</b>
        <br>Terdapat mading kreasi sebagai display pameran kreasi anak/masyarakat di bidang  literasi atau ketrampilan lainnya, terdapat lemari yang berisi mainan edukatif sebagi penunjang kegiatan edukasi di TBM. Ruang kreasi juga sebagai tempat apresiasi dari karya literasi (misalnya mendongeng).
        <br><b>Ruang Edukasi</b>
        <br>Kegiatan ini merupakan kegiatan bersama yang melibatkan banyak pihak, sehingga keberhasilan kegiatan ini tidak akan terwujud tanpa dukungan, bantuan dan kerjasama dari berbagai pihak dalam penyelenggaran.</p>

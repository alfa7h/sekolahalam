        <h4>Belajar Public Speaking</h4>
        <p class="indent"><b>Harta Karun</b></p>
        <p class="indent"><i>Hape-ku</i> bergetar menandakan ada pesan masuk yang harus ku baca dari teman 1 SMA , 1 kamar <i>kost</i> dan 1 tempat kuliahku panggil saja dia Eneng alias Rosinta.</p>
        <p class="indent">"Nun, rumah lo di Villa Mutiara Gading 1 kan? Tau sekolah Alam Anak Soleh ga?"</p> 
        <p class="indent">Begitulah pesan pertama sebagai salah satu jalan menuju "harta karun" surganya dunia.</p>
        <img class="img-artikel" src="images/agenda/belajar-public-speaking/img1.jpg">
        <p class="indent">Lebay? Tidak. Ini bicara soal hati yang sebenarnya tidak dapat di tuangakan lewat rangkaian kata. Aku percaya, kebahagiaan yang sesungguhnya dapat dirasakan setelah melewati proses yang rumit seperti kisah cinta *eaaaaa~~~ </p>        
        <p class="indent">Sama halnya dengan kami yang melewati perjalanan yang “pajang” alias nyasar, cuaca yang membuat kasur kita lebih posesif dan lain-lain yang berkeliaran di pikiran usia muda kita yang hampir menghilangkan niat untuk bertemu "harta karun".</p>
        <p class="indent">Akhir Januari di tahun 2016, kami disambut dengan senyuman yang tulus dari 3 sosok yaitu Kepala Sekolah Bapak Khair, Oom Agus dan Mbak Prita. Kami langsung jatuh cinta dengan semua yang diucapkan oleh 3 sosok tersebut ditambah dengan backsound dari suara dan canda tawa anak-anak SDAAS makin membuat kami merasa......... "Yeah!! It’s the REAL Purpose of Life...!!"</p>
        <p class="indent">Alhamdulillah, hari Sabtu di ujung bulan Januari tanggal 30 kami diberikan kesempatan untuk berbagai wawasan tentang "Public Speaking"..Kenapa oh kenapa? Hmm... karena Rosinta mempunyai latar belakang kuliah dengan jurusan Komunikasi yang mungkin masih bisa bermanfaat untuk adik-adikku di Sekolah Anak Soleh.</p>
        <p class="indent">Kami berdua saat itu memberikan "pemanasan" materi tentang pengertian dan faktor yang harus diperhatikan dari Public Speaking. Yayyy! Adik-adikku antusias mengikuti cara pembelajaran dari kami berdua. Namun, ada hal yang membuat kami (Aku dan Rosinta) malah justru belajar dari salah satu murid di sana.</p>
        <p class="indent">Namanya Iwan, Ia adalah murid yang paling unik di sana yang menjadi pusat perhatian kami. Ia berbeda dari yang lain memiliki kebutuhan khusus karena down syndrome yang dihadapinya. Umurnya paling bannyak di antara yang lain yaitu 18 tahun. Tetapi ia memiliki kelebihan yang murid lain tidak miliki, ia sangat percaya diri.</p>
        <p class="indent">Iwan yang paling semangat dan pertama menunjukkan jari telunjuknya ke udara saat Rosinta menanyakan, "ayo, siapa yang berani maju ke depan untuk memperkenalkan diri???"</p>
        <p class="indent">Perkenalan nama, tempat tinggal, hobi, cita-cita dan kegiatan yang di lakukan sehari-hari menjadi perkenalan singkat dari adik-adik yang maju di depan kelas.</p>
        <p class="indent">Macam-macam sekali karakter adik-adikku di sana. Ada yang pemalu, ada yang super aktif, ada yang gugup dan lain-lain yang membuat kami semakin jatuh cinta terhadap mereka.</p>
        <p class="indent">Bahagia. Satu kata yang kami rasakan saat itu, yang baru kami rasakan selama 22 tahun kami ada di dunia. Kami baru bisa bermanfaat untuk orang-orang yang sangat mebutuhkan uluran niat baik kita. Tapi tidak ada kata terlambat untuk jadi yang lebih baik.</p>
        <p class="indent">Terima kasih adik-adikku, terima kasih Sekolah Alam Anak Soleh sudah memberikan warna yang berbeda di hidup kami.</p>

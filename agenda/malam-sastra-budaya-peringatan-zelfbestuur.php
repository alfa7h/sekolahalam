        <h4>Malam Sastra Budaya Peringatan Zelfbestuur</h4>
        <img class="img-artikel" src="images/agenda/malam-sastra-budaya-peringatan-zelfbestuur/img1.jpg">
        <p>Dalam rangka menyongsong Peringatan 100 Tahun Zelfbestuur (1916-2016), Rumah Baca (Rumba) H.O.S. Tjokroaminoto bekerja sama dengan Rumah Peneleh dan Syarikat Islam menggelar Malam Apresiasi Sastra dan Budaya.</p>
        <p>Para penerima tamu adalah penggiat rumba yang kini menjadi panitia acara "haul 100 tahun" pidato H.O.S. Tjokroaminoto di tahun 1916 lalu. Pidato HOS Tjokro muda dengan berani menyampaikan ide mengenai Zelfbestuur, kesadaran nasionalisme yang menolak segala bentuk penjajahan, Indonesia merdeka yang memerintah diri sendiri.</p> 
        <p class="kutipan">"Tuan-tuan jangan takut, bahwa kita dalam rapat ini berani mengucapkan perkataan zelfbestuur atau pemerintahan sendiri...Supaya Hindia (Indonesia) lekas dapat pemerintahan sendiri (zelfbestuur)..." </p>
        <img class="" src="images/agenda/malam-sastra-budaya-peringatan-zelfbestuur/img2.jpg">
        <p>Malam itu teras depan rumba HOS diubah menjadi ruang pameran, foto-foto Kamera Lubang Jarum karya Sekolah Alam Anak Soleh menghias setiap sudutnya, stand SMP Terbuka Ilalang memperlihatkan hasil kerajinan tangan berbahan koran bekas yang menjadi vas bunga, tempat payung, kap lampu, tempat tisu dlsb. Acara Malam Apresiasi Sastra Budaya ini dibuka dengan menyanyikan lagu Indonesia Raya oleh seluruh hadirin, kemudian disusul Tari Ronggeng Blantek oleh 3 siswi SMAN 1 Tarumajaya. Acara berlanjut dengan berturut-turut pembacaan puisi karya Ajip Rosidi, Taufiq Ismail dst. Mendapat kesempatan pertama membaca puisi adalah Syafinuddin Al-Mandari dengan puisi "Sudah lama aku jadi tuhan", dilanjutkan oleh Ketua Umum Pimpinan Pusat Wanita Syarikat Islam Ibu Valina Singka Subekti yang membacakan puisi Syair Orang Lapar karya Taufiq Ismail, kemudian Pak Aulia Tahkim membawakan puisi karyanya, lalu Bu Ella Jagad, disambung Haji Aty Cancer Zein membacakan puisi Kepada Arwah H.O.S Tjokroaminoto karya Almarhum Buya Hamka dan seterusnya. </p>
        <img src="images/agenda/malam-sastra-budaya-peringatan-zelfbestuur/img3.jpg">
        <p>Lebih dari setengah acara diisi dengan kegiatan pembacaan puisi, tarian dan nyanyian. Suasana kesenian lebih pekat, tata pencahayaan dipercayakan oleh panitia kepada kepada rekan-rekan Teater Zat Komunitas Seni bidang Teater di Jurusan Bahasa dan Sastra Indonesia, Universitas Negeri Jakarta yang juga mengiringi lagu-lagu dengan gitar, Pak Syifa menambah kekayaan musik dengan gesekan biolanya.</p>

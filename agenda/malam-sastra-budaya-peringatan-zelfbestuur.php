        <script async src="//platform.twitter.com/widgets.js" charset="utf-8"></script>
        <p>Puncak acara adalah pidato kebudayaan oleh Hamdan Zoelva, Ketua Umum Lajnah Tanfidziyah Syarikat Islam (SI), mantan Ketua Mahkamah Konstitusi Indonesia periode 2013-2015. Dalam pidatonya beliau kembali mengingatkan mengenai sejarah munculnya ide pencetusan Zelfbestuur, kondisi-kondisi yang menyebabkan dan relevansinya dengan kondisi bangsa saat ini. </p>
        <p>Dalam kesempatan tersebut beliau berpesan, bahwa kemerdekaan yang sudah dengan susah payah diwujudkan oleh para pendahulu agar tidak disia-siakan, agar diisi dengan prestasi-prestasi yang mengutamakan kesejahteraan rakyat Indonesia. Upaya itu hanya dapat dilakukan jika generasi muda memiliki kualitas yang unggul di segala bidang. Hal mana yang kembali mengingatkan saya akan kalimat HOS Tjokroaminoto <b>"Setinggi tinggi 
        ilmu, semurni murni tauhid, sepintar pintar siasat"</b>.</p>

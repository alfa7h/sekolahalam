        <h4>Jambore Relawan Sekolah Raya 2015</h4>
        <p >Jambore Relawan Sekolah Raya 2015 yang mengusung tema "Merekam Jejak Relawan",  merupakan satu kesatuan dan keberlanjutan dari Jambore Relawan Sekolah Raya 2014 yang bertema "<i>Visioning</i> Sekolah Raya".</p>
        <p>"Merekam Jejak Relawan" bertujuan untuk menuliskan kembali pengalaman-pengalaman para relawan pendidikan di Indonesia, mempelajari polanya, untuk kemudian pola-pola tersebut diformulasikan menjadi suatu desain kurikulum pendidikan. Desain kurikulum pendidikan yang akan dihasilkan tentunya diharapkan sejalan dengan semangat Sekolah Raya dalam mewujudkan basis pendidikan dengan media utama adalah alam dan budaya.</p>
        
        <p>Jika tahun lalu sebagai tahun pertama penyelenggaran, Jambore Relawan Sekolah Raya diikuti oleh 50 peserta yang baru sebatas berasal dari wilayah Jakarta, Bekasi, dan Malang saja. Di tahun ini mencapai 100 peserta yang memberikan konfirmasi kehadiran, tidak hanya datang dari kawan-kawan penggiat literasi dan pendidikan (Rumah Baca dan Komunitas) yang berdomisili di Jabodetabek. Tidak juga hanya dari kawan-kawan di Pulau Jawa seperti Banten, Bandung, Cirebon, Kediri, dan Malang. Melainkan juga dari kawan-kawan yang datang langsung dari Kalimantan Tengah, dan Sulawesi Tengah, dan perwakilan kawan-kawan di Nusa Tenggara Timur, hingga Papua.</p> 
        <p>Sesuai dengan tema "Merekam Jejak Relawan", maka pada Jambore Relawan tahun ini Sekolah Raya menghadirkan sejumlah narasumber dengan kompetensi dibidang kepenulisan. Mereka adalah Muhammad Farhan (presenter), Ahmad Fuadi (penulis), Fauzan Mukrim (penulis), Stanley Meulen (penulis), Indah Julianti Sibarani (blogger), dan Yosep (jurnalis). Dan Ridho “Cak Ipoel” Saiful yang karena satu dan lain hal di gantikan Mbak Henny Budi Hastuti sebagai fasilitator yang akan memandu berjalannya acara Jambore Relawan Sekolah Raya 2015 di hari kedua.</p>

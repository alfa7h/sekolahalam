        <h4>Mahasiswa Korea Jadi Relawan</h4>
        <img src="images/agenda/mahasiswa-korea-jadi-relawan/img1.jpg" class="responsive-img img-artikel2" >
        <p class="indent">KBRN, Bekasi : Untuk merayakan Natal, enam orang mahasiswa dari Chatolic University of Daegu Korea, menjadi relawan selama dua hari di Sekolah Alam Anak Soleh di Desa Setia Asih, Tarumajaya, Kabupaten Bekasi, Jawa Barat. Enam orang mahasiswa tersebut, dua diantaranya asal Indonesia, berbaur dengan para pengajar di sekolah gratis yang dikhususkan bagi anak dhuafa dan yatim piatu tersebut.</p>
        <p class="indent">Mereka mengajarkan berbagai hal, dari mulai bahasa Korea, menulis dengan huruf Korea, menggambar dan menyanyi lagu Korea khusus untuk anak-anak.</p>
        <p class="indent">"Selama dua hari sejak tanggal 23 Desember, mahasiswa asal Korea menjadi relawan pengajar. Kegiatannya lebih banyak fun saja untuk menghibur anak-anak," kata Abdul Khair, Kepsek Sekolah Alam Anak Soleh, Rabu (24/12/2014).</p>
        <p class="indent">Awalnya, lanjut Khair, mahasiswa asal Korea sempat ragu, apalagi meraka akan menjadi relawan di sekolah yang memang menerapkan sistem Islami dalam pengajarannya. Namun, setelah memastikan sendiri, mereka akhirnya tahu bahwa tingkat toleransi di Indonesia sudah bisa terjaga dengan baik.</p>
        <img src="images/agenda/mahasiswa-korea-jadi-relawan/img2.jpg" class="responsive-img img-artikel2" >
        <p class="indent">"Kita cukup senang, apalagi kegiatan mereka cukup memberi manfaat bagi anak-anak," ujarnya.</p> 
        <p class="indent">Selama menjadi relawan, mahasiswa asal Chatolic University of Daegu Korea, menginap di hotel dan ada juga yang menginap di rumah mahasiswa asal Indonesia. Setelah menjadi relawan di Sekolah Alam Anak Soleh, mahasiswa asal Korea tersebut akan mengunjungi sejumlah lokasi wisata di Jogjakarta untuk merayakan Tahun Baru.</p>
        <p class="indent">Menurut Danika (19), mahasiswi Chatolic University of Daegu Korea, kegiatan relawan ini dibiayai kampus tempatnya menempuh ilmu di jurusan Visual Design. Awalnya Danika dan lima rekannya  mengajukan proposal untuk menjadi relawan di Indonesia.</p>
        <p class="indent">Dari 30 kelompok yang mengajukan proposal, kelompok Danika merupakan satu dari enam kelompok yang dipilih oleh kampus. Kebanyakan setiap kelompok mengajukan diri menjadi relawan di berbagai Negara di Asia. "Seperti di Indonesia, Taiwan, Vietnam, Kamboja, Nepal dan Indonesia," kata mahasiswi asal Indonesia yang tinggal di Kelapa Gading Jakarta Utara tersebut.</p>
        <p class="indent">Moment Natal dan Tahun Baru dipilih, karena jadwalnya bertepatan dengan liburan musim dingin di Korea. "Sekalian liburan ke Indonesia, kita juga ingin berbagi secara sosial dengan siapa saja untuk merayakan Natal," katanya lagi.</p>
        <p class="indent">Dalam kegiatan ini Mahasiswa dari Chatolic University of Daegu Korea, juga membagikan sejumlah alat perlengkapan sekolah bagi siswa di Sekolah Alam Anak Soleh. Sementara, mahasiswa asal Korea juga diajarkan oleh alumni Sekolah Alam, cara membuat Kamera Lubang Jarum (KLJ) dengan media kaleng dan kardus bekas.</p>
        <p class="indent">"Saya senang ada kegiatan seperti ini. Apalagi kita jadi tahu banyak tentang Negeri Korea. Semoga pelajar ataupun mahasiswa dari Negara lainnya juga tertarik menjadi relawan di Sekolah Alam. Saya juga senang bisa bertukar ilmu dengan mereka," kata Nugraha, salah seorang alumni Sekolah Alam Anak Soleh. (EH)</p>
        <p class="indent">Sumber: RRI 24 Desember 2014</p>
